package Clases;

public class Valor {

    private int farenheith;
    private int revival;
    private int elAlquimista;

    public Valor(){
        this.farenheith = 5000;
        this.revival = 12000;
        this.elAlquimista = 45000;
    }

    public int getFarenheith(){
        return farenheith;
    }

    public int getRevival(){
        return revival;
    }

    public int getElAlquimista(){
        return elAlquimista;
    }
}
